# Task 4
# Создать словарь. Ключ - случайное целое число от 1 до 10, значение - количество раз, сколько число выпало. Заполнить словарь циклом. 

# Импортируем функцию священного рандома
import random

# Создаем пустой словарь для хранения полученных результатов
counts = {}

# Количество итераций (т.е. повторений программы)
iterations = 100

for i in range(iterations):
    # Генерируем случайное целое число в диапазоне от 1 до 10
    num = random.randint(1, 10)
    
    # Увеличиваем счетчик для данного числа на 1 или создаем новую "карточку"
    if num in counts:
        counts[num] += 1
    else:
        counts[num] = 1

print(counts)

