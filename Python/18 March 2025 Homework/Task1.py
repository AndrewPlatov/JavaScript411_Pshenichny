# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–≥–æ —Å–æ—Å—Ç–æ—è–Ω–∏—è —Å–ª–æ–≤–∞
def display_word(answer, guessed_letters):
    return ''.join(letter if letter in guessed_letters else '*' for letter in answer)


# ------------------------------------------–î–ª—è –≤–µ—Ä—Å–∏–∏ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤-------------------------------------------------- #

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤ –≤ —Ä—É—Å—Å–∫–∏–µ (–ø—Ä–æ–±–ª–µ–º–∞ –≤ —Ç–æ–º, —á—Ç–æ –Ω–µ–∫–æ—Ä—ã–µ –±—É–∫–≤—ã –Ω–∞ –∫–∏—Ä–∏–ª–ª–∏—Ü–µ —Ç—Ä–µ–±—É—é—Ç –Ω–∞–ø–∏—Å–∞–Ω–∏—è –¥–≤—É—Ö –∏ –±–æ–ª–µ–µ 
# –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤) {–ö–û–ö–û–ô –£–ñ–û–°, –ü–ê–ú–ê–ì–ò–¢–ò}

def convert_latin_to_russian(guess):
    converted = ''                      # –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –∫–∞–∫ –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É, –≤ –∫–æ—Ç–æ—Ä—É—é –±—É–¥–µ—Ç –¥–æ–±–∞–≤–ª—è—Ç—å—Å—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    i = 0                               # —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≤ 0, —á—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å —Å –ø–µ—Ä–≤–æ–≥–æ —Å–∏–º–≤–æ–ª–∞ —Å—Ç—Ä–æ–∫–∏ guess
    
    # –ê —Ç–µ–ø–µ—Ä—å –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –ø–æ–ª–Ω–µ–π—à–∞—è –∂–µ—Å—Ç—å (–∏–±–æ –±—ã–ª–∞ —Å–ª–æ–º–∞–Ω–∞ –ø–∞—Ä–∞ –∏–∑–≤–∏–ª–∏–Ω –∏ –ø—Ä–æ—á–∏—Ç–∞–Ω–∞ —Å—Ç–∞—Ç—å—è)
    
    # –¶–∏–∫–ª –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è, –ø–æ–∫–∞ i –º–µ–Ω—å—à–µ –¥–ª–∏–Ω—ã —Å—Ç—Ä–æ–∫–∏ guess. –≠—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –ø—Ä–æ–π—Ç–∏ –ø–æ –í–°–ï–ú —Å–∏–º–≤–æ–ª–∞–º —Å—Ç—Ä–æ–∫–∏
    while i < len(guess):
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å –≤–∑—è—Ç—å –¥–≤–∞ —Å–∏–º–≤–æ–ª–∞ (—Ç–µ–∫—É—â–∏–π –∏ —Å–ª–µ–¥—É—é—â–∏–π) –∏–∑ —Å—Ç—Ä–æ–∫–∏ (—Ç–æ –µ—Å—Ç—å –Ω–µ –≤—ã—Ö–æ–¥–∏—Ç –ª–∏ –∏–Ω–¥–µ–∫—Å –∑–∞ –ø—Ä–µ–¥–µ–ª—ã —Å—Ç—Ä–æ–∫–∏).
        # –ï—Å–ª–∏ –¥–≤–∞ —Å–∏–º–≤–æ–ª–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, "sh", "ch") –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç –≤ —Å–ª–æ–≤–∞—Ä–µ latin_to_russian, —Ç–æ –∫ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–º—É –∑–Ω–∞—á–µ–Ω–∏—é –∏–∑ —Å–ª–æ–≤–∞—Ä—è 
        # –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ —Å—Ç—Ä–æ–∫—É converted –∏ –∏–Ω–¥–µ–∫—Å i —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ 2, —á—Ç–æ–±—ã –ø—Ä–æ–ø—É—Å—Ç–∏—Ç—å —É–∂–µ –æ–±—Ä–∞–±–æ—Ç–∞–Ω–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã.
        if i + 1 < len(guess) and guess[i:i+2] in latin_to_russian:
            converted += latin_to_russian[guess[i:i+2]]
            i += 2
        
        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —Å–∏–º–≤–æ–ª (–æ–¥–∏–Ω —Å–∏–º–≤–æ–ª) –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ —Å–ª–æ–≤–∞—Ä–µ latin_to_russian, —Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤ —Å—Ç—Ä–æ–∫—É –∏ 
        # –∏–Ω–¥–µ–∫—Å i —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –Ω–∞ 1.
        elif guess[i] in latin_to_russian:
            converted += latin_to_russian[guess[i]]
            i += 1
        
        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —Å–∏–º–≤–æ–ª –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ —Å–ª–æ–≤–∞—Ä–µ latin_to_russian, –æ–Ω –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è –≤ —Å—Ç—Ä–æ–∫—É –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π.
        # –ò–Ω–¥–µ–∫—Å i —Ç–∞–∫–∂–µ —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ 1.
        else:
            converted += guess[i]
            i += 1
    
    # –ü–æ—Å–ª–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è —Ü–∏–∫–ª–∞ —Ñ—É–Ω–∫—Ü–∏—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É
    return converted

# -------------------------------------------------------------------------------------------------------------------------------------- #

# –ê —Ç–µ–ø–µ—Ä—å —è —Ö–æ—á—É —Å—ã–≥—Ä–∞—Ç—å —Å –í–∞–º–∏ –≤ –æ–¥–Ω—É –∏–≥—Ä—É üòà
def play_game():
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π –≤–æ–ø—Ä–æ—Å –∏ –æ—Ç–≤–µ—Ç
    question_text, answer = random.choice(list(question.items()))  
    guessed_letters = set()     # –°–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è —É–≥–∞–¥–∞–Ω–Ω—ã—Ö –±—É–∫–≤
    attempts = 6                # –ó–∞–¥–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫

    print(f"–í–æ–ø—Ä–æ—Å: {question_text}")
    
    # –¢–µ–ø–µ—Ä—å –ø—Ä–æ–ø–∏—à–µ–º —Ü–∏–∫–ª, –∫–æ—Ç—Ä—ã–π –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è, –ø–æ–∫–∞ —É "–∂–µ—Ä—Ç–≤—ã" –æ—Å—Ç–∞—é—Ç—Å—è –ø–æ–ø—ã—Ç–∫–∏
    while attempts > 0:
        current_display = display_word(answer, guessed_letters)
        print(f"–°–ª–æ–≤–æ: {current_display}")
        
        guess = input("–í–≤–µ–¥–∏—Ç–µ –±—É–∫–≤—É –∏–ª–∏ —Å–ª–æ–≤–æ: ").strip().lower()
        
        # –ü—Ä–æ–≤–µ—Ä–∏–º, –≤–æ–∑–º–æ–∂–Ω–æ —Å–ª–æ–≤–æ –æ—Ç–≥–∞–¥–∞–Ω–Ω–æ —Å—Ä–∞–∑—É —Ü–µ–ª–∏–∫–æ–º
        if len(guess) > 1:
            if guess == answer.lower():
                print("–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã —É–≥–∞–¥–∞–ª–∏ —Å–ª–æ–≤–æ!")
                break
            else:
                print("–ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ —Å–ª–æ–≤–æ!")
                attempts -= 1
        else:
            # –õ–∏–±–æ –∂–µ –∏–≥—Ä–æ–∫ –æ—Ç–≥–∞–¥—ã–≤–∞–µ—Ç –ø–æ –æ–¥–Ω–æ–π –±—É–∫–≤–µ
            if guess in answer.lower():
                guessed_letters.add(guess)     # –ï—Å–ª–∏ —É–≥–∞–¥–∞–ª –±—É–∫–≤—É, –¥–æ–±–∞–≤–ª—è–µ–º –µ–µ –≤ –æ—Ç–≥–∞–¥–∞–Ω–Ω—ã–µ
                print("–í—ã —É–≥–∞–¥–∞–ª–∏ –±—É–∫–≤—É!")
            else:
                print("–ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–∞—è –±—É–∫–≤–∞!")
                attempts -= 1
        
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –≤—ã–∏–≥—Ä—ã—à
        if all(letter in guessed_letters for letter in answer.lower()):
            print(f"–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã —É–≥–∞–¥–∞–ª–∏ —Å–ª–æ–≤–æ: {answer}")
            break
        
        print(f"–û—Å—Ç–∞–ª–æ—Å—å –ø–æ–ø—ã—Ç–æ–∫: {attempts}")
    
    # –ï—Å–ª–∏ –ø–æ–ø—ã—Ç–∫–∏ –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å –∏ —Å–ª–æ–≤–æ –Ω–µ –æ—Ç–≥–∞–¥–∞–Ω–Ω–æ
    if attempts == 0:
        print(f"–í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏! –ó–∞–≥–∞–¥–∞–Ω–Ω–æ–µ —Å–ª–æ–≤–æ: {answer}")

# –ó–∞–ø—É—Å–∫ –∏–≥—Ä—ã
play_game()
    