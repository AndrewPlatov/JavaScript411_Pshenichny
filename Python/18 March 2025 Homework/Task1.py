from pool1 import question

# ------------------------------------------–î–ª—è –≤–µ—Ä—Å–∏–∏ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤-------------------------------------------------- #

# –ü–µ—Ä–µ–≤–æ–¥–∏–º –ª–∞—Ç–∏–Ω—Å–∫–∏–µ –±—É–∫–≤—ã –≤ –∞–Ω–∞–ª–æ–≥ –Ω–∞ –∫–∏—Ä–∏–ª–ª–∏—Ü–µ (–ø—Ä–æ–±–ª–µ–º–∞ —Å –±—É–∫–≤–æ–π '—ë')
latin_to_russian = {
    'a': '–∞', 'b': '–±', 'v': '–≤', 'g': '–≥', 'd': '–¥', 'e': '–µ', '—ë': '—ë',
    'zh': '–∂', 'z': '–∑', 'i': '–∏', 'y': '–π', 'k': '–∫', 'l': '–ª', 'm': '–º',
    'n': '–Ω', 'o': '–æ', 'p': '–ø', 'r': '—Ä', 's': '—Å', 't': '—Ç', 'u': '—É',
    'f': '—Ñ', 'h': '—Ö', 'c': '—Ü', 'ch': '—á', 'sh': '—à', 'shch': '—â', 'y': '—ã',
    'e': '—ç', 'yu': '—é', 'ya': '—è'
}
